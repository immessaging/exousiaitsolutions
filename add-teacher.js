    // Country change handler
    if (countrySelect) {
        countrySelect.addEventListener("change", () => {
            const selectedCountry = countrySelect.value;
            
            // Reset state and LGA selects
            stateSelect.innerHTML = '<option value="">Select State</option>';
            lgaSelect.innerHTML = '<option value="">Select LGA</option>';
            
            if (selectedCountry === "Nigeria") {
                populateStates();
                stateSelect.disabled = false;
            } else {
                stateSelect.disabled = true;
                lgaSelect.disabled = true;
            }
            
            // Always disable LGA until state is selected
            lgaSelect.disabled = true;
        });
    }

    // State change handler
    if (stateSelect) {
        stateSelect.addEventListener("change", () => {
            const selectedState = stateSelect.value;
            
            if (selectedState && nigeriaStates[selectedState]) {
                populateLGAs(selectedState);
                lgaSelect.disabled = false;
            } else {
                lgaSelect.innerHTML = '<option value="">Select LGA</option>';
                lgaSelect.disabled = true;
            }
        });
    }

    // Populate states dropdown
    function populateStates() {
        stateSelect.innerHTML = '<option value="">Select State</option>';
        
        Object.keys(nigeriaStates).forEach(state => {
            const option = document.createElement("option");
            option.value = state;
            option.textContent = state;
            stateSelect.appendChild(option);
        });
    }

    // Populate LGAs dropdown
    function populateLGAs(state) {
        lgaSelect.innerHTML = '<option value="">Select LGA</option>';
        
        if (nigeriaStates[state]) {
            nigeriaStates[state].forEach(lga => {
                const option = document.createElement("option");
                option.value = lga;
                option.textContent = lga;
                lgaSelect.appendChild(option);
            });
        }
    }

    // File preview functionality
    function setupFilePreview(inputId, previewId, allowedTypes, maxSizeKB) {
        const input = document.getElementById(inputId);
        const preview = document.getElementById(previewId);
        
        if (input && preview) {
            input.addEventListener('change', function(event) {
                const file = event.target.files[0];
                
                if (file) {
                    // Validate file type
                    if (!allowedTypes.includes(file.type)) {
                        alert(`Only ${allowedTypes.join(', ')} files are allowed.`);
                        event.target.value = '';
                        preview.style.display = 'none';
                        return;
                    }
                    
                    // Validate file size
                    if (file.size > maxSizeKB * 1024) {
                        alert(`File size must not exceed ${maxSizeKB}KB.`);
                        event.target.value = '';
                        preview.style.display = 'none';
                        return;
                    }
                    
                    // Create preview
                    const reader = new FileReader();
                    reader.onload = function(e) {
                        preview.src = e.target.result;
                        preview.style.display = 'block';
                    };
                    reader.readAsDataURL(file);
                } else {
                    preview.src = '#';
                    preview.style.display = 'none';
                }
            });
        }
    }

    // Set up file previews
    setupFilePreview('passport', 'passportPreview', ['image/jpeg', 'image/png'], 900);
    setupFilePreview('medical_certificate', 'medicalCertificatePreview', ['image/jpeg', 'image/png', 'application/pdf'], 1024);
    setupFilePreview('recommendation_letter', 'recommendationLetterPreview', ['image/jpeg', 'image/png', 'application/pdf'], 1024);

    // Form submission
    if (submitBtn && form) {
        submitBtn.addEventListener("click", function(e) {
            e.preventDefault();
            
            if (!validateStep()) return;

            const formData = new FormData(form);
            
            // Add CSRF token if needed
            const csrfToken = document.querySelector('[name="csrf_token"]')?.value;
            if (csrfToken) {
                formData.append('csrf_token', csrfToken);
            }

            fetch(form.action, {
                method: "POST",
                body: formData,
            })
            .then(response => {
                if (!response.ok) throw new Error("Network response was not ok");
                return response.json();
            })
            .then(data => {
                if (data.success) {
                    alert("Application submitted successfully!");
                    if (data.redirect) {
                        window.location.href = data.redirect;
                    }
                } else {
                    alert("Error: " + (data.message || "Unknown error occurred"));
                }
            })
            .catch(error => {
                console.error("Error:", error);
                alert("An error occurred while submitting your application.");
            });
        });
    }

    // Initialize Nigeria states if Nigeria is selected by default
    if (countrySelect && countrySelect.value === "Nigeria") {
        populateStates();
    }
